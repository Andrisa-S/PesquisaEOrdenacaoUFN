    public String chamarSelecionados(){
        List<String> metodosSelecionados = new ArrayList();
        
        if (chbBolha.isSelected()){
            metodosSelecionados.add("Bolha");
        }
        if (chbSelecao.isSelected()){
            metodosSelecionados.add("Seleção");
        }
        if (chbPente.isSelected()){
            metodosSelecionados.add("Pente");
        }
        if (chbInsercao.isSelected()){
            metodosSelecionados.add("Inserção");
        }
        if (chbAgitacao.isSelected()){
            metodosSelecionados.add("Agitação");
        }
        
        return metodosSelecionados.toString();
    }
    
    private void carregarArquivo() {
        JFileChooser fileChooser = new JFileChooser();
        if (fileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            File arquivo = fileChooser.getSelectedFile();
            List<Integer> numeros = lerArquivo(arquivo);

            // Executar métodos de ordenação
            List<Resultado> resultados = new ArrayList<>();

            resultados.add(Metodos.bolha(numeros));
            resultados.add(Metodos.selecao(numeros));
            resultados.add(Metodos.insercao(numeros));
            resultados.add(Metodos.agitacao(numeros));
            resultados.add(Metodos.pente(numeros));

            // Mostrar resultados em nova janela
            Saida rf = new Saida(resultados);
            rf.setVisible(true);
        }
    }

    private List<Integer> lerArquivo(File arquivo) {
        List<Integer> numeros = new ArrayList<>();
        try (Scanner sc = new Scanner(arquivo)) {
            while (sc.hasNextInt()) {
                numeros.add(sc.nextInt());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao ler arquivo: " + e.getMessage());
        }
        return numeros;
    }
