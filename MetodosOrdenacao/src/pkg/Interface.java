    private void btnExecutarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExecutarActionPerformed
        txtSelecionados.setText(chamarSelecionados());

    if (numerosCarregados == null || numerosCarregados.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Nenhum arquivo foi carregado!");
        return;
    }

    List<Resultado> resultados = new ArrayList<>();

    if (chbBolha.isSelected()) {
    resultados.add(Metodos.bolha(new ArrayList<>(numerosCarregados)));
    }
    if (chbSelecao.isSelected()) {
        resultados.add(Metodos.selecao(new ArrayList<>(numerosCarregados)));
    }
    if (chbInsercao.isSelected()) {
        resultados.add(Metodos.insercao(new ArrayList<>(numerosCarregados)));
    }
    if (chbAgitacao.isSelected()) {
        resultados.add(Metodos.agitacao(new ArrayList<>(numerosCarregados)));
    }
    if (chbPente.isSelected()) {
        resultados.add(Metodos.pente(new ArrayList<>(numerosCarregados)));
    }

    if (resultados.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Selecione ao menos um método de ordenação!");
        return;
    }

    // Abre a janela de saída com os resultados
    Saida saida = new Saida(resultados);
    saida.setLocationRelativeTo(this); // centraliza
    saida.setVisible(true);
        
    }//GEN-LAST:event_btnExecutarActionPerformed

